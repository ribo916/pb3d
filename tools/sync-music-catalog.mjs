import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, '..');
const MUSIC_ROOT = path.join(ROOT, 'music', 'active');
const OUT = path.join(ROOT, 'music', 'catalog.js');
const AUDIO_EXTS = new Set(['.wav', '.mp3', '.ogg', '.m4a', '.aac']);

function slug(value) {
  return value
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '') || 'track';
}

function words(value) {
  return value
    .replace(/[_-]+/g, ' ')
    .replace(/\s+/g, ' ')
    .trim()
    .split(' ')
    .filter(Boolean)
    .map((part) => part.charAt(0).toUpperCase() + part.slice(1).toLowerCase())
    .join(' ');
}

function genreLabel(name) {
  return name.replace(/[_-]+/g, ' ').trim().toUpperCase();
}

function parseTrackName(base) {
  if (base.includes(' - ')) {
    const parts = base.split(' - ');
    return {
      artist: parts[0].trim(),
      label: parts.slice(1).join(' - ').trim()
    };
  }
  return { artist: '', label: words(base) };
}

function collectTracks(root) {
  if (!fs.existsSync(root)) return [];
  const genres = fs.readdirSync(root, { withFileTypes: true })
    .filter((entry) => entry.isDirectory())
    .map((entry) => entry.name)
    .sort();

  const tracks = [];
  for (const genre of genres) {
    const dir = path.join(root, genre);
    const files = fs.readdirSync(dir, { withFileTypes: true })
      .filter((entry) => entry.isFile() && AUDIO_EXTS.has(path.extname(entry.name).toLowerCase()))
      .map((entry) => entry.name)
      .sort();
    for (const fileName of files) {
      const parsed = path.parse(fileName);
      const meta = parseTrackName(parsed.name);
      tracks.push({
        key: slug(genre + '-' + parsed.name),
        genre: slug(genre),
        genreLabel: genreLabel(genre),
        label: meta.label || words(parsed.name),
        artist: meta.artist,
        file: path.posix.join('music', 'active', genre, fileName)
      });
    }
  }
  return tracks;
}

function writeCatalog(tracks, outFile) {
  const content = [
    '/* Auto-generated by tools/sync-music-catalog.mjs. */',
    'export const MUSIC_TRACKS = [',
    ...tracks.map((track) => {
      return '  ' + JSON.stringify(track).replace(/"([^"]+)":/g, '$1:') + ',';
    }),
    '];',
    ''
  ].join('\n');
  fs.writeFileSync(outFile, content);
}

const tracks = collectTracks(MUSIC_ROOT);
writeCatalog(tracks, OUT);
console.log('synced', path.relative(ROOT, OUT), 'with', tracks.length, 'tracks');
